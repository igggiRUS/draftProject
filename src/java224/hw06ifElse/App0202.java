package java224.hw06ifElse;

public class App0202 {
	public static void main(String[] args) {
		// @
		// @ @
		// @ @ @
		// @ @ @ @
		// @ @ @ @ @
		
		final int size = 5;
		for (int i = 0; i < size; i++) {
			for (int j = 0; j <= i; j++) {
				System.out.print("@ ");
			}
			
			System.out.print('\n');
		}
	}
}
